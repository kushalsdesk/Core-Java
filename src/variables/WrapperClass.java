package variables;

/**
 * WrapperClass
 *
 * provides a wat to use primitive data types as Reference data types, contains
 * some useful methods that can be used with collections
 *
 * Primitive Wrapper
 * boolean Boolean
 * char Character
 * int Integer
 * double Double
 *
 *
 * autoboxing where the compiler automatically do a conversion between the
 * primitive to the corresponding Object Wrapper Classes,
 * reverse is unboxing
 */
public class WrapperClass {

  public static void main(String[] args) {
    System.out.println("Welcome to the Wrapper");

    // Autoboxing, compiler does the job
    Boolean result = true;
    Character b = 'b';
    Integer c = 123;
    Double d = 3.14;
    String name = "Kushal";

    // Methods for wrappers class

  }
}
