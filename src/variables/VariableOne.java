package variables;

public class VariableOne {
  public static void main(String[] args) {
    System.out.println("Welcome to Variables in Java");

    // Declaration
    int x;

    // Assignment
    x = 123;

    // Initialization
    int y = 123;

    System.out.println("The Values of x and y is: " + x + "," + y);

    double pi = 3.14;
    char symbol = 'p';
    String name = "Kushal";

    System.out.println("double pi: " + pi + ", char symbol:" + symbol + ", String name:" + name);
  }
}
